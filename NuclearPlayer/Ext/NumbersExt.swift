import SwiftUI

extension Int {
    var asCGFloat: CGFloat {
        return CGFloat(self)
    }
}

extension Double {
    var asCGFloat: CGFloat {
        return CGFloat(self)
    }
}

extension Float {
    var asCGFloat: CGFloat {
        return CGFloat(self)
    }
}
